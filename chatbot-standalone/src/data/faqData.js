// Function to search FAQs by keyword
export function searchFAQs(query) {
  if (!query || query.length < 2) return [];
  
  const searchTerms = query.toLowerCase().split(' ');
  
  return faqData.filter(faq => {
    const searchText = (faq.question + ' ' + faq.answer + ' ' + faq.keywords.join(' ')).toLowerCase();
    
    return searchTerms.some(term => searchText.includes(term));
  }).sort((a, b) => {
    // Sort by relevance (number of matching keywords)
    const aMatches = searchTerms.filter(term => 
      (a.question + ' ' + a.answer + ' ' + a.keywords.join(' ')).toLowerCase().includes(term)
    ).length;
    const bMatches = searchTerms.filter(term => 
      (b.question + ' ' + b.answer + ' ' + b.keywords.join(' ')).toLowerCase().includes(term)
    ).length;
    
    return bMatches - aMatches;
  });
}

// Function to get FAQs by category
export function getFAQsByCategory(category) {
  return faqData.filter(faq => faq.category === category);
}

// Function to get random FAQs (for featured questions)
export function getRandomFAQs(count = 3) {
  const shuffled = [...faqData].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}

